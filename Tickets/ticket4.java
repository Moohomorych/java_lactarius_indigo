/*
Напишите метод hasOdd, который принимает множество (Set) целых чисел в качестве параметра и возвращает true, если набор содержит хотя бы одно нечетное целое число, и false в противном случае. Если передано пустое множество, ваш метод должен вернуть false.
*/

import java.util.Set;
public class SetOperations {
    public static boolean hasOdd(Set<Integer> set) {
        // Проверяем, не пусто ли множество
        if (set.isEmpty()) {
            return false;
        }
        // Перебираем элементы множества
        for (int number : set) {
            // Проверяем, является ли текущее число нечетным
            if (number % 2 != 0) {
                return true; // Если хотя бы одно нечетное число найдено, возвращаем true
            }
        }
        // Если не найдено нечетных чисел, возвращаем false
        return false;
    }
}
